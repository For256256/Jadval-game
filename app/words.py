CLEAN_WORDS = []
_seen = set()
for w, c in WORDS:
    nw = normalize(w)
    if nw and nw not in _seen and all('\u0600' <= ch <= '\u06FF' for ch in nw):
        _seen.add(nw)
        CLEAN_WORDS.append((nw, c))
