def normalize(word: str) -> str:
    """یکدست‌سازی حروف عربی به فارسی و حذف فاصله/نیم‌فاصله."""
    word = word.replace("ي", "ی").replace("ك", "ک")
    word = word.replace("ﻻ", "لا").replace("\u200c", "").replace(" ", "")
    word = word.replace("أ", "ا").replace("إ", "ا").replace("آ", "ا").replace("ة", "ه")
    return word.strip()


